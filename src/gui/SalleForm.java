    public void loadSalle() {
        model.setRowCount(0);
        for (Salle ss : s.findAll()) {
            model.addRow(new Object[]{
                ss.getId(),
                ss.getNom(),
                ss.getType(),
                ss.getCapacite()
            });
        }

    }

