    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String nom = txtNom.getText().toString();
        String type = txtType.getText().toString();
        int capacite = 0;
        try {
            capacite = Integer.parseInt(txtCapacite.getText().toString());
        } catch (NumberFormatException e) {
            System.err.println("La capacité doit être un nombre entier.");
            return;
        }
        if (s.create(new Salle(nom, type, capacite))) {
            JOptionPane.showMessageDialog(this, "Bien enregistré");
            loadSalle();
        } else {
            JOptionPane.showMessageDialog(this, "Probléme");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer cette salle ?" + s.findById(id));
        if (response == 0) {
            s.delete(s.findById(id));
            loadSalle();
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment modifier cette salle ?");
        if (response == 0) {
            String nom = txtNom.getText().toString();
            String type = txtType.getText().toString();
            int capacite = Integer.parseInt(txtCapacite.getText().toString());
            if (s.update(new Salle(id, nom, type, capacite))) {
                JOptionPane.showMessageDialog(this, "Bien enregistrée");
                loadSalle();
            } else {
                JOptionPane.showConfirmDialog(this, "Problème");
            }
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void tabSalleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabSalleMouseClicked
        // TODO add your handling code here:

        if (tabSalle.getSelectedRow() != -1) {
            // Mettre à jour l'ID de la salle sélectionnée
            id = Integer.parseInt(tabSalle.getValueAt(tabSalle.getSelectedRow(), 0).toString());
            System.out.println("id salle : " + id);
            // Remplir les champs de texte avec les données de la salle sélectionnée
            txtNom.setText(tabSalle.getValueAt(tabSalle.getSelectedRow(), 1).toString());
            txtType.setText(tabSalle.getValueAt(tabSalle.getSelectedRow(), 2).toString());
            txtCapacite.setText(tabSalle.getValueAt(tabSalle.getSelectedRow(), 3).toString());
        }


    }//GEN-LAST:event_tabSalleMouseClicked


